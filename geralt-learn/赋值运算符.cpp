#include <iostream>
using namespace std;
 
int main() {
   // 赋值运算法符
   // =
   int a =10;
   a = 100;
   cout << "a = "<< a << endl;
   
   // +=
   a = 10;
   a += 2; 
   cout << "a = "<< a << endl;
   
   // -=
   a = 10;
   a -= 2;
   cout << "a = "<< a << endl;
   
   // *=
   a = 10;
   a *= 2;
   cout << "a = "<< a << endl;
   
   // /=
   a = 10;
   a /= 2;
   cout << "a = "<< a << endl;   
   
   // %=
   a = 10;
   a %= 3;
   cout << "a = "<< a << endl;
   
   return 0;
}
